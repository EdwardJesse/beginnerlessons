const weather = prompt("Choose your weather type today: 'sun, snow, rain.");

if (weather === "sun") {
    console.log("It's sunny, so I'll go swimming.")
}   else if (weather === "snow") {
    console.log("It's snowing, so I'll go sledding.")
}   else if (weather === "rain") {
    console.log("It's raining, so I'll read a book.")    
}   else {
    console.log("I'll figure out what to do.")
}
